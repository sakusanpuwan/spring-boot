SELECT 
    c.CHARACTER_ID, 
    c.CHARACTER_NAME, 
    m.MOVIE_ID, 
    m.MOVIE_NAME, 
    m.RELEASE_DATE, 
    m.DIRECTOR, 
    p.PHASE_NAME
FROM CHARACTERS c
INNER JOIN MOVIES m ON c.MOVIE_ID = m.MOVIE_ID
INNER JOIN PHASES p ON c.PHASE_ID = p.PHASE_ID
WHERE c.CHARACTER_ID = 1;  


CREATE TABLE CHARACTERS (
    ID NUMBER PRIMARY KEY,  -- Unique identifier for each row in the file
    MOVIE_ID NUMBER NOT NULL,  -- Foreign key to MOVIES table
    CHARACTER_ID NUMBER NOT NULL,  -- Represents unique characters across movies
    CHARACTER_NAME VARCHAR2(255) NOT NULL,  -- Character's full name
    ACTOR VARCHAR2(255) NOT NULL,  -- Actor who played the role
    PHASE_ID NUMBER NOT NULL,  -- Foreign key to PHASES table
    CONSTRAINT fk_characters_movies FOREIGN KEY (MOVIE_ID) 
        REFERENCES MOVIES(MOVIE_ID) ON DELETE CASCADE,
    CONSTRAINT fk_characters_phases FOREIGN KEY (PHASE_ID) 
        REFERENCES PHASES(PHASE_ID) ON DELETE CASCADE
);






