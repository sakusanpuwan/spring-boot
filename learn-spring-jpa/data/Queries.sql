SELECT
    uc.table_name,
    uc.constraint_name,
    uc.constraint_type,
    LISTAGG(ucc.column_name, ', ') WITHIN GROUP (ORDER BY ucc.column_name) AS columns,
    uc.r_constraint_name,
    uc_ref.table_name AS referenced_table
FROM
    user_constraints uc
JOIN
    user_cons_columns ucc ON uc.constraint_name = ucc.constraint_name
LEFT JOIN
    user_constraints uc_ref ON uc.r_constraint_name = uc_ref.constraint_name
WHERE
    uc.constraint_type IN ('P', 'R')
GROUP BY
    uc.table_name,
    uc.constraint_name,
    uc.constraint_type,
    uc.r_constraint_name,
    uc_ref.table_name
ORDER BY
    uc.table_name,
    uc.constraint_name;
    
